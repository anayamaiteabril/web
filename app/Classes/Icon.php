    public static function toSelect($object = null)
    {
        $selectedIcon = ($object) ? $object->icon : '';
        $html = '<select id="icon" name="icon" class="selectpicker form-control">';
        foreach (self::$values as $icon) {
            $selected = ($selectedIcon == $icon) ? 'selected' : '';
            $html .=  '<option value="'.$icon.'" data-icon="'.$icon.'" '.$selected.'>'.$icon.'</option>';
        }
        $html .= '</select>';
        return $html;
    }
